class DataOnly{
	int i;
	double d;
	boolean b;
	
	public static void main(String[] args){
		DataOnly dataonly = new DataOnly();
		dataonly.i = 47;
		dataonly.d = 1.1;
		dataonly.b = false;
		
		System.out.println(dataonly.i);
		System.out.println(dataonly.d);
		System.out.println(dataonly.b);

	}//End of main
}//End of class